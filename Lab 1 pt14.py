#Exercise 14: Using pow() to calculate powers in Python
print("6 to the power of 3 is: " + str(pow(6, 3)))
print("-254 to the power of 5 is: " + str(pow(-254, 5)))
print("-12 to the power of -8 is: " + str(pow(-12, -8)))
print("5 to the power of 3, mod 4 is: " + str(pow(5, 3, 4)))
print("11 to the power of 6, mod 8 is: " + str(pow(11, 6, 8)))
